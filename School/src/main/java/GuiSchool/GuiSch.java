    private void showOption(String title, Runnable actionStudent, Runnable actionTeacher) {
    String[] options = { "Estudiante", "Maestro" };
     ImageIcon iconUser = new ImageIcon("C:\\Users\\DELL\\OneDrive\\Escritorio\\user.png");
    int  oSelect = JOptionPane.showOptionDialog(
        this,
        "Selecciona una de las dos:",
        "Usuarios",
        JOptionPane.DEFAULT_OPTION,
        JOptionPane.QUESTION_MESSAGE,
        iconUser,
        options,
        options[0]
    );
    
    if (oSelect == 0) {
        actionStudent.run();
    } else if (oSelect == 1) {
        actionTeacher.run();
    }
}
    
    
    
